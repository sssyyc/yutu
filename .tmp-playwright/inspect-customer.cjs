const { chromium } = require("playwright-core");
(async () => {
  const browser = await chromium.launch({ headless: true, executablePath: 'C:/Program Files (x86)/Microsoft/Edge/Application/msedge.exe' });
  const page = await browser.newPage({ viewport: { width: 1531, height: 1012 } });
  const logs = [];
  page.on('console', msg => logs.push(`console:${msg.type()}:${msg.text()}`));
  page.on('pageerror', err => logs.push(`pageerror:${err.message}`));
  await page.goto('http://127.0.0.1:5174/login', { waitUntil: 'domcontentloaded' });
  await page.locator('input').nth(0).fill('merchant01');
  await page.locator('input').nth(1).fill('123456');
  await page.locator('button:has-text("登录系统")').click();
  await page.waitForTimeout(2000);
  await page.goto('http://127.0.0.1:5174/merchant/customer/list', { waitUntil: 'networkidle' });
  await page.waitForTimeout(1500);
  const url = page.url();
  const bodyText = await page.locator('body').innerText();
  const html = await page.content();
  const merchantMainCount = await page.locator('.merchant-main').count();
  const moduleCount = await page.locator('.admin-module-page').count();
  const tableCount = await page.locator('.el-table').count();
  console.log('URL=' + url);
  console.log('merchantMainCount=' + merchantMainCount);
  console.log('moduleCount=' + moduleCount);
  console.log('tableCount=' + tableCount);
  console.log('BODY_START');
  console.log(bodyText.slice(0, 1500));
  console.log('BODY_END');
  console.log('HTML_START');
  console.log(html.slice(0, 4000));
  console.log('HTML_END');
  console.log('LOGS_START');
  console.log(logs.join('\n'));
  console.log('LOGS_END');
  await browser.close();
})();
